function add(a,b) {
  return a + b;
}

function subtract(a,b) {
  return a - b;
}

function multiply(a,b) {
  return a * b;
}

function divide(a,b) {
  return a / b;
}

function increment(n) {
  return ++n;
}

function decrement(n) {
  return --n;
}

function makeInt(n) {
var n = '0x2328'
  parseInt('n', 10);
  return n;

}



function preserveDecimal(n) {
var n = '2.222'
  parseFloat('n');
  return n;

}
