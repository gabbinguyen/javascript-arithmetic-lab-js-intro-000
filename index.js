function add(a,b) {
  return a + b;
}

function subtract(a,b) {
  return a - b;
}

function multiply(a,b) {
  return a * b;
}

function divide(a,b) {
  return a / b;
}

function increment(n) {
  return ++n;
}

function decrement(n) {
  return --n;
}

function makeInt(n) {
  parseInt('n', 10);
  return n;
  var n = '0x2328'
}



function preserveDecimal(n) {
  parseFloat('n');
  return n;
  var n = '2.222'
}
